
def sum_all_numbers(num1, num2):
    return num1 + num2


def sum_only_positive_numbers(num1, num2):
    if num1 > 0 and num2 > 0:
        return num1 + num2
    else:
        return None


def sum_only_negative_numbers(num1, num2):
    if num1 < 0 and num2 < 0:
        return num1 + num2
    else:
        return None
